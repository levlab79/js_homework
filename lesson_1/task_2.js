// Разные типы переменных.
// Тип Number
let myNumber = 243;
myNumber = 412.2;
alert(myNumber);

// infinity
let myInfinity = 2 / 0;
alert(myInfinity);

// Тип String.
let myString = "Какая-то строка";
alert(myString);

// Тип Boolean.
let myBool = true;
myBool = false;
alert(myBool);

// NaN
let myNan = "some string" * 32;
alert(myNan);

// null
let myNull = null;
alert(myNull);

// undefined
let strangeValue;
alert(strangeValue);
